@Component("LIC14274_PJT1002_PE_NLP73cdca1e-71ed-4a0e-a1c1-1cfe628d715e")
public class Element_is_not_displayed implements Nlp {
	@InputParams({ @InputParam(name = "xpath", type = "java.lang.String")})
	@ReturnType(name = "IsDisplayed", type = "java.lang.Boolean")

	@Override
	public List<String> getTestParameters() throws NlpException {
		List<String> params = new ArrayList<>();
		return params;
	}

	@Override
	public StringBuilder getTestCode() throws NlpException {
		StringBuilder sb = new StringBuilder();
		return sb;
	}

	@Override
	public NlpResponseModel execute(NlpRequestModel nlpRequestModel) throws NlpException {

		NlpResponseModel nlpResponseModel = new NlpResponseModel();
		Map<String, Object> attributes = nlpRequestModel.getAttributes();
		WebDriver driver=nlpRequestModel.getWebDriver();

		Boolean IsDisplayed = null;
		try {
			String element = (String) attributes.get("xpath");
			IsDisplayed = driver.findElement(By.xpath(element)).isDisplayed();
             IsDisplayed=true;
			nlpResponseModel.setStatus(CommonConstants.pass);
			nlpResponseModel.setMessage("Element Is Dispalyed In WebPage");
		} catch (Exception e) {
			IsDisplayed = false;
			nlpResponseModel.setStatus(CommonConstants.pass);
			nlpResponseModel.setMessage("Element Is Not Dispalyed In WebPage");

		}

		nlpResponseModel.getAttributes().put("IsDisplayed", IsDisplayed);
		return nlpResponseModel;
	}
}